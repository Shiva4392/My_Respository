
public class Datatypes_Primitive {

	public static void main(String[] args) {
		
		byte a=100;
		short s=200;
		int i=28765645;
		int ii=-356865464;
		long mobileno=2047742876;
		float f=4545.545f;
		double d=354554.564354d;
		boolean b=true;
		boolean bb=false;
		char c = 'a';
		String st="fjfbmb";
		System.out.println("Byte Value = "+a);
		System.out.println("Short Value = "+s);
		System.out.println("Int Value = "+i);
		System.out.println("Int Value = "+ii);
		System.out.println("Long Value = "+mobileno);
		System.out.println("Float Value = "+f);
		System.out.println("Double Value = "+d);
		System.out.println("Boolean Value = "+b);
		System.out.println("Boolean Value = "+bb);
		System.out.println("Character Value = "+c);
		System.out.println("String Value = "+st);
		
	}

}
